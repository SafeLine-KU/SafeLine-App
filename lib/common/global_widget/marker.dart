class MarkerInfo {
  final String name;
  final String address;
  final String area;
  final double distance;
  final int groundFloor;
  final int basementFloor;

  MarkerInfo({
    required this.name,
    required this.address,
    required this.area,
    required this.distance,
    required this.groundFloor,
    required this.basementFloor,
  });
}
